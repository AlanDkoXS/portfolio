  const switchLanguages = () => {
    const languageToggle = document.getElementById('language-toggle');
  
    languageToggle.addEventListener('change', function () {
      const lang = this.checked ? 'es' : 'en'; // Cambia el idioma según el estado del checkbox
  

      document.querySelector('.navbar__link[href="#home"]').textContent = translations.navHome[lang]; // "Home" o "Inicio"
      document.querySelector('.navbar__link[href="#about"]').textContent = translations.navAbout[lang]; // "About me" o "Sobre mí"
      document.querySelector('.navbar__link[href="#skills"]').textContent = translations.navSkills[lang]; // "Skills" o "Habilidades"
      document.querySelector('.navbar__link[href="#projects"]').textContent = translations.navProjects[lang]; // "Projects" o "Proyectos"
      document.querySelector('.navbar__link[href="#contact"]').textContent = translations.navContact[lang]; // "Contact" o "Contacto"
      document.querySelector('.home__title').textContent = translations.homeTitle[lang];
      document.querySelector('.home__description').textContent = translations.homeDescription[lang];
      document.querySelector('.btn.btn--primary').textContent = translations.resumeButton[lang];
      document.querySelector('#about .section__title').textContent = translations.aboutTitle[lang]; // Selector específico para "About"
      document.querySelector('.about__description').textContent = translations.aboutDescription[lang];
      document.querySelector('#skills .section__title').textContent = translations.skillsTitle[lang]; // Selector específico para "Skills"
      document.querySelector('.skills__description').textContent = translations.skillsDescription[lang];
      document.querySelector('.skills__tags-title').textContent = translations.skillsTagsTitle[lang];
      
      const skillsTags = document.querySelectorAll('.skills__tag');
      skillsTags.forEach((tag, index) => {
        tag.textContent = translations.skillsTags[lang][index];
      });
      

      const skillsCards = document.querySelectorAll('.skills__card-container');
      skillsCards.forEach((card, index) => {
        const title = card.querySelector('.skills__card-title');
        const description = card.querySelector('.skills__card-description');
      
        title.textContent = translations[`skillCard${index + 1}Title`][lang];
        description.textContent = translations[`skillCard${index + 1}Description`][lang];
      });

      document.querySelector('#projects .section__title').textContent = translations.projectsTitle[lang];
      document.querySelector('#contact .section__title').textContent = translations.contactTitle[lang]; // "Contact" o "Contacto"
      document.querySelector('.contact__form-title').textContent = translations.contactFormTitle[lang]; // "Send Message" o "Enviar Mensaje"
      document.querySelector('.contact__form-label[for="name"]').textContent = translations.nameLabel[lang]; // "Name" o "Nombre"
      document.querySelector('.contact__form-label[for="email"]').textContent = translations.emailLabel[lang]; // "Email" o "Correo Electrónico"
      document.querySelector('.contact__form-label[for="subject"]').textContent = translations.subjectLabel[lang]; // "Subject" o "Asunto"
      document.querySelector('.contact__form-label[for="message"]').textContent = translations.messageLabel[lang]; // "Text" o "Texto"
      document.querySelector('#modal').textContent = translations.submitSuccessMessage[lang]; // Mensaje de éxito
      document.querySelector('#btn_close-modal').textContent = translations.closeButton[lang]; // "Close" o "Cerrar"
    });
  };
  
  export default switchLanguages;
  